#Determine la potencia ingresada por teclado de un número ingresado por teclado.
numero1 = int(input("Ingrese un 1er número: "))
numero2 = int(input("Ingrese un 1er número: "))

numero_potencia = numero1 ** numero2

print(f"El numero Base es: {numero1}")
print(f"El número Potencia es: {numero2}")
print(f"El número base con la potencia es: {numero_potencia}")